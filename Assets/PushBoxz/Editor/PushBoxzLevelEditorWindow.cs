        private void CountGeneratedActionKinds(out int moveCount, out int pullCount)
        {
            moveCount = 0;
            pullCount = 0;
            for (var i = 0; i < generatedReverseActions.Count; i++)
            {
                if (generatedReverseActions[i].kind == AutoActionKind.Pull)
                {
                    pullCount++;
                }
                else
                {
                    moveCount++;
                }
            }
        }

        private void DrawAutoWeightSlider(GUIContent label, int weightIndex)
        {
            EditorGUI.BeginChangeCheck();
            var value = Quantize01(EditorGUILayout.Slider(label, GetAutoWeight(weightIndex), 0f, 1f));
            if (EditorGUI.EndChangeCheck())
            {
                SetAutoWeight(weightIndex, value);
            }
        }

        private float GetAutoWeight(int weightIndex)
        {
            switch (weightIndex)
            {
                case 0:
                    return autoContinueWeight;
                case 1:
                    return autoChangeDirectionWeight;
                case 2:
                    return autoChangeBoxWeight;
                default:
                    return 0f;
            }
        }

        private void SetAutoWeight(int changedIndex, float value)
        {
            value = Quantize01(value);
            var remaining = 1f - value;
            var firstOther = changedIndex == 0 ? 1 : 0;
            var secondOther = changedIndex == 2 ? 1 : 2;
            var firstOtherValue = Mathf.Clamp01(GetAutoWeight(firstOther));
            var secondOtherValue = Mathf.Clamp01(GetAutoWeight(secondOther));
            var otherTotal = firstOtherValue + secondOtherValue;

            SetAutoWeightRaw(changedIndex, value);
            if (otherTotal <= 0.0001f)
            {
                SetAutoWeightRaw(firstOther, Quantize01(remaining * 0.5f));
                SetAutoWeightRaw(secondOther, Quantize01(remaining - GetAutoWeight(firstOther)));
            }
            else
            {
                SetAutoWeightRaw(firstOther, Quantize01(remaining * firstOtherValue / otherTotal));
                SetAutoWeightRaw(secondOther, Quantize01(remaining - GetAutoWeight(firstOther)));
            }

            NormalizeAutoWeights();
        }

        private void SetAutoWeightRaw(int weightIndex, float value)
        {
            switch (weightIndex)
            {
                case 0:
                    autoContinueWeight = value;
                    break;
                case 1:
                    autoChangeDirectionWeight = value;
                    break;
                case 2:
                    autoChangeBoxWeight = value;
                    break;
            }
        }

        private void NormalizeAutoWeights()
        {
            var continueWeight = Mathf.Max(0f, autoContinueWeight);
            var changeDirectionWeight = Mathf.Max(0f, autoChangeDirectionWeight);
            var changeBoxWeight = Mathf.Max(0f, autoChangeBoxWeight);
            var total = continueWeight + changeDirectionWeight + changeBoxWeight;
            if (total <= 0.0001f)
            {
                autoContinueWeight = 0.4f;
                autoChangeDirectionWeight = 0.3f;
                autoChangeBoxWeight = 0.3f;
                return;
            }

            autoContinueWeight = Quantize01(continueWeight / total);
            autoChangeDirectionWeight = Quantize01(changeDirectionWeight / total);
            autoChangeBoxWeight = Quantize01(1f - autoContinueWeight - autoChangeDirectionWeight);
        }

        private static float Quantize01(float value)
        {
            return Mathf.Clamp01(Mathf.Round(value * 10f) / 10f);
        }

