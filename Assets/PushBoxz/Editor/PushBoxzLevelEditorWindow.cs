        private int GetBoxSpreadScore(List<Vector2Int> boxStates, int boxIndex, Vector2Int nextBoxPosition)
        {
            var score = 0;
            for (var i = 0; i < boxStates.Count; i++)
            {
                if (i == boxIndex)
                {
                    continue;
                }

                var other = boxStates[i];
                var dx = nextBoxPosition.x - other.x;
                var dy = nextBoxPosition.y - other.y;
                score += Mathf.Min(16, dx * dx + dy * dy);
            }

            return score;
        }

