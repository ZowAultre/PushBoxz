        private void PaintCell(Vector2Int position)
        {
            ClearGeneratedSolution();
            switch (activeBrush)
            {
                case Brush.Floor:
                    tiles[position.x, position.y] = BaseTileType.Floor;
                    break;
                case Brush.Wall:
                    tiles[position.x, position.y] = BaseTileType.Wall;
                    goals.Remove(position);
                    boxes.Remove(position);
                    if (hasPlayer && playerStart == position)
                    {
                        hasPlayer = false;
                    }
                    break;
                case Brush.Goal:
                    tiles[position.x, position.y] = BaseTileType.Floor;
                    goals.Add(position);
                    break;
                case Brush.Box:
                    tiles[position.x, position.y] = BaseTileType.Floor;
                    if (hasPlayer && playerStart == position)
                    {
                        hasPlayer = false;
                    }
                    boxes.Add(position);
                    break;
                case Brush.Player:
                    tiles[position.x, position.y] = BaseTileType.Floor;
                    boxes.Remove(position);
                    playerStart = position;
                    hasPlayer = true;
                    editorPlayerFacing = Direction.Up;
                    break;
                case Brush.Erase:
                    tiles[position.x, position.y] = BaseTileType.Floor;
                    goals.Remove(position);
                    boxes.Remove(position);
                    if (hasPlayer && playerStart == position)
                    {
                        hasPlayer = false;
                    }
                    break;
                default:
                    throw new ArgumentOutOfRangeException();
            }
        }

        private void ClearGeneratedSolution()
        {
            generatedReverseActions.Clear();
