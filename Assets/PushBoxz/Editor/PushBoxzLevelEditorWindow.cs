        private void HandleReverseDesignInput()
        {
            if (EditorGUIUtility.editingTextField)
            {
                return;
            }

            var current = Event.current;
            if (current == null || current.type != EventType.KeyDown)
            {
                return;
            }

            var handled = true;
            switch (current.keyCode)
            {
                case KeyCode.W:
                    TryEditorModeMove(Direction.Up);
                    break;
                case KeyCode.S:
                    TryEditorModeMove(Direction.Down);
                    break;
                case KeyCode.A:
                    TryEditorModeMove(Direction.Left);
                    break;
                case KeyCode.D:
                    TryEditorModeMove(Direction.Right);
                    break;
                case KeyCode.F:
                    if (reverseDesignMode)
                    {
                        TryReverseModePull();
                    }
                    else
                    {
                        TryEditorModePush();
                    }
                    break;
                default:
                    handled = false;
                    break;
            }

            if (!handled)
            {
                return;
            }

            SaveDraftState();
            current.Use();
            Repaint();
        }

        private void TryEditorModeMove(Direction direction)
        {
            editorPlayerFacing = direction;

            if (!hasPlayer)
            {
                SetMessage("编辑移动需要一个玩家初始点。", MessageType.Warning);
                return;
            }

            var target = playerStart + DirectionUtility.ToOffset(direction);
            if (!CanEditorPlayerOccupy(target))
            {
                SetMessage("编辑移动被阻挡：" + FormatPosition(target) + "。", MessageType.Info);
                return;
            }

            ClearGeneratedSolution();
            playerStart = target;
            SetMessage("编辑移动：玩家移动到 " + FormatPosition(playerStart) + "。", MessageType.Info);
        }

        private void TryReverseModePull()
        {
            if (!hasPlayer)
            {
                SetMessage("倒拉模式需要一个玩家初始点。", MessageType.Warning);
                return;
            }

            var facingOffset = DirectionUtility.ToOffset(editorPlayerFacing);
            var boxPosition = playerStart + facingOffset;
            var playerTarget = playerStart - facingOffset;

            if (!boxes.Contains(boxPosition))
            {
                SetMessage("倒拉失败：玩家正前方没有箱子。", MessageType.Info);
                return;
            }

            if (!CanEditorPlayerOccupy(playerTarget))
            {
                SetMessage("倒拉失败：玩家身后一格被阻挡 " + FormatPosition(playerTarget) + "。", MessageType.Info);
                return;
            }

            ClearGeneratedSolution();
            boxes.Remove(boxPosition);
            boxes.Add(playerStart);
            playerStart = playerTarget;
            SetMessage("倒拉成功：箱子移动到 " + FormatPosition(playerStart + facingOffset) + "。", MessageType.Info);
        }

        private void TryEditorModePush()
