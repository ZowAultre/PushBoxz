        private List<LevelSolutionStep> BuildForwardSolutionSteps()
        {
            var steps = new List<LevelSolutionStep>();
            for (var i = generatedReverseActions.Count - 1; i >= 0; i--)
            {
                var action = generatedReverseActions[i];
                if (action.kind == AutoActionKind.Move)
                {
                    steps.Add(new LevelSolutionStep
                    {
                        kind = LevelSolutionStepKind.Move,
                        boxId = -1,
                        direction = OppositeDirection(action.direction),
                        playerFrom = action.playerTo,
                        playerTo = action.playerFrom,
                        boxFrom = Vector2Int.zero,
                        boxTo = Vector2Int.zero
                    });
                    continue;
                }

                steps.Add(new LevelSolutionStep
                {
                    kind = LevelSolutionStepKind.Push,
                    boxId = action.boxId,
                    direction = action.direction,
                    playerFrom = action.playerTo,
                    playerTo = action.boxTo,
                    boxFrom = action.boxTo,
                    boxTo = action.boxFrom
                });
            }

            return steps;
        }

        private void RestoreGeneratedActionsFromSolution(LevelDataAsset asset)
        {
            generatedReverseActions.Clear();
            playbackStartBoxes.Clear();
            playbackStartPlayer = playerStart;
            solutionPlaybackIndex = -1;

            if (asset.solutionSteps == null || asset.solutionSteps.Count == 0)
            {
                return;
            }

            foreach (var box in boxes)
            {
                playbackStartBoxes.Add(box);
            }

            for (var i = asset.solutionSteps.Count - 1; i >= 0; i--)
            {
                var step = asset.solutionSteps[i];
                if (step == null)
                {
                    continue;
                }

                if (step.kind == LevelSolutionStepKind.Move)
                {
                    generatedReverseActions.Add(new AutoGeneratedAction
                    {
                        kind = AutoActionKind.Move,
                        boxId = -1,
                        direction = OppositeDirection(step.direction),
                        playerFrom = step.playerTo,
                        playerTo = step.playerFrom,
                        boxFrom = Vector2Int.zero,
                        boxTo = Vector2Int.zero
                    });
                    continue;
                }

                generatedReverseActions.Add(new AutoGeneratedAction
                {
                    kind = AutoActionKind.Pull,
                    boxId = step.boxId,
                    direction = step.direction,
                    playerFrom = step.playerTo,
                    playerTo = step.playerFrom,
                    boxFrom = step.boxTo,
                    boxTo = step.boxFrom
                });
            }
        }

        private void SaveLevel()
        {
            var issues = ValidateCurrentLevel();
            if (issues.Count > 0)
            {
                SetMessage("请先修复校验问题，再保存关卡。", MessageType.Warning);
                return;
            }

            EnsureLevelsFolder();

            var asset = loadedAsset;
            if (asset == null)
            {
                asset = CreateLevelAsset(levelId);
            }

            ApplyToAsset(asset);
            var renameError = TryRenameAssetToLevelId(asset);
            EditorUtility.SetDirty(asset);
            AssetDatabase.SaveAssets();
            AssetDatabase.Refresh();
            PushBoxzLevelRegistryBuilder.RebuildRegistryAsset();
            Selection.activeObject = asset;
            if (string.IsNullOrEmpty(renameError))
            {
                SetMessage("已保存关卡资源：" + AssetDatabase.GetAssetPath(asset), MessageType.Info);
            }
            else
            {
                SetMessage("已保存关卡资源，但自动重命名失败：" + renameError, MessageType.Warning);
            }
        }

