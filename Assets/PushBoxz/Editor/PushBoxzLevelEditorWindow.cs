        private void ShowLevelAssetLoadMenu()
        {
            EnsureLevelsFolder();
            var guids = AssetDatabase.FindAssets("t:LevelDataAsset", new[] { LevelsFolder });
            if (guids.Length == 0)
            {
                SetMessage("没有找到可加载的 Level Data Asset。", MessageType.Info);
                return;
            }

            var menu = new GenericMenu();
            foreach (var guid in guids
                .Select(AssetDatabase.GUIDToAssetPath)
                .OrderBy(path => path, StringComparer.OrdinalIgnoreCase))
            {
                var path = guid;
                var asset = AssetDatabase.LoadAssetAtPath<LevelDataAsset>(path);
                if (asset == null)
                {
                    continue;
                }

                var label = string.IsNullOrWhiteSpace(asset.levelId)
                    ? Path.GetFileNameWithoutExtension(path)
                    : asset.levelId;
                var selectedAsset = asset;
                menu.AddItem(new GUIContent(label + "    " + path), selectedAsset == loadedAsset, () => LoadFromAsset(selectedAsset));
            }

            menu.ShowAsContext();
        }

