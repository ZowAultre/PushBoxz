        private void GenerateRandomReverseLevel()
        {
            var random = autoUseRandomSeed ? new System.Random(Environment.TickCount) : new System.Random(autoSeed);
            if (autoUseRandomSeed)
            {
                autoSeed = Environment.TickCount;
                random = new System.Random(autoSeed);
            }

            width = Mathf.Clamp(width, MinSize, MaxSize);
            height = Mathf.Clamp(height, MinSize, MaxSize);
            autoWidth = width;
            autoHeight = height;
            autoReverseStepCount = AutoGenerationStepBudget;
            hasPlayer = false;
            boxes.Clear();
            goals.Clear();
            generatedReverseActions.Clear();
            playbackStartBoxes.Clear();
            solutionPlaybackIndex = -1;
            CreateFilledGrid(BaseTileType.Floor);

            var totalCells = width * height;
            var requestedBoxes = Mathf.Clamp(autoBoxCount, 1, Mathf.Min(8, Mathf.Max(1, totalCells)));
            var boxStates = new List<Vector2Int>();
            var targetPositions = new List<Vector2Int>();
            var visited = new HashSet<Vector2Int>();
            var used = new HashSet<Vector2Int>();
            var memory = new AutoGenerationMemory();

            for (var i = 0; i < requestedBoxes; i++)
            {
                if (!TryGetRandomFreeCell(random, used, out var position))
                {
                    break;
                }

                used.Add(position);
                goals.Add(position);
                boxStates.Add(position);
                targetPositions.Add(position);
                visited.Add(position);
                RecordBoxPosition(memory, i, position);
                RegisterVisitedCell(memory, position);
            }

            if (boxStates.Count == 0)
            {
                SetMessage("自动生成失败：没有足够空间放置箱子。", MessageType.Warning);
                return;
            }

            AddRandomInitialWalls(random, used);

            var currentBoxIndex = random.Next(boxStates.Count);
            if (!TryGetRandomStandSide(random, boxStates, targetPositions, currentBoxIndex, out var playerPosition, out var pullDirection))
            {
                SetMessage("自动生成失败：找不到可站立的初始箱子侧边。", MessageType.Warning);
                return;
            }

            if (!CanReachEveryGoal(playerPosition))
            {
                SetMessage("自动生成失败：玩家无法到达所有目的地，请重新生成。", MessageType.Warning);
                return;
            }

            visited.Add(playerPosition);
            RegisterVisitedCell(memory, playerPosition);
            var rollbackCount = 0;
            var snapshots = new List<AutoGenerationSnapshot>();
            var completedSteps = 0;
            var guardCount = 0;
            while (completedSteps < autoReverseStepCount && guardCount++ < autoReverseStepCount * AutoGenerationGuardMultiplier)
            {
                var snapshot = CreateAutoGenerationSnapshot(boxStates, visited, memory, currentBoxIndex, playerPosition, pullDirection);
                var actionChoice = PickAutoAction(random);
                var acted = false;

                if (actionChoice == 2 && boxStates.Count > 1)
                {
                    acted = TryAutoSwitchBox(random, boxStates, targetPositions, visited, memory, ref currentBoxIndex, ref playerPosition, out pullDirection);
                }

                if (!acted && actionChoice == 1)
                {
                    acted = TryAutoChangeDirection(random, boxStates, targetPositions, visited, memory, currentBoxIndex, ref playerPosition, out pullDirection);
                }

                if (!acted)
                {
                    acted = TryAutoPullCurrentBox(boxStates, targetPositions, visited, memory, currentBoxIndex, ref playerPosition, pullDirection, true);
                }

                if (!acted)
                {
                    acted = TryAutoChangeDirection(random, boxStates, targetPositions, visited, memory, currentBoxIndex, ref playerPosition, out pullDirection)
                        || TryAutoSwitchBox(random, boxStates, targetPositions, visited, memory, ref currentBoxIndex, ref playerPosition, out pullDirection);
                }

                if (!acted)
                {
                    if (TryRollbackAutoGeneration(random, snapshots, boxStates, ref visited, ref memory, ref currentBoxIndex, ref playerPosition, ref pullDirection))
                    {
                        rollbackCount++;
                        completedSteps = Mathf.Max(0, completedSteps - 1);
                        continue;
                    }

                    break;
                }

                snapshots.Add(snapshot);
                if (snapshots.Count > AutoGenerationRollbackWindow)
                {
                    snapshots.RemoveAt(0);
                }

                completedSteps++;
            }

            CompressGeneratedReverseActions();
            FillUnvisitedWalls(random, visited, boxStates);

            playerStart = playerPosition;
            hasPlayer = true;
            boxes.Clear();
            for (var i = 0; i < boxStates.Count; i++)
            {
                boxes.Add(boxStates[i]);
            }

            playbackStartPlayer = playerStart;
            playbackStartBoxes.Clear();
            playbackStartBoxes.AddRange(boxStates);
            SaveDraftState();
            CountGeneratedActionKinds(out var moveCount, out var pullCount);
            SetMessage("自动生成完成。Seed: " + autoSeed + "，回退：" + rollbackCount + "，总动作：" + generatedReverseActions.Count + "，移动：" + moveCount + "，拉箱子：" + pullCount + "。", MessageType.Info);
            Repaint();
        }

        private bool CanReachEveryGoal(Vector2Int start)
        {
            if (!IsWalkable(start))
            {
                return false;
            }

            var reachable = new HashSet<Vector2Int>();
            var frontier = new Queue<Vector2Int>();
            reachable.Add(start);
            frontier.Enqueue(start);

            while (frontier.Count > 0)
            {
                var current = frontier.Dequeue();
                foreach (var next in GetNeighbors(current))
                {
                    if (reachable.Contains(next) || !IsWalkable(next))
                    {
                        continue;
                    }

                    reachable.Add(next);
                    frontier.Enqueue(next);
                }
            }

            foreach (var goal in goals)
            {
                if (!reachable.Contains(goal))
                {
                    return false;
                }
            }

            return true;
        }

        private AutoGenerationSnapshot CreateAutoGenerationSnapshot(
            List<Vector2Int> boxStates,
            HashSet<Vector2Int> visited,
            AutoGenerationMemory memory,
            int currentBoxIndex,
            Vector2Int playerPosition,
            Direction pullDirection)
        {
            return new AutoGenerationSnapshot
            {
                actionCount = generatedReverseActions.Count,
                currentBoxIndex = currentBoxIndex,
                playerPosition = playerPosition,
                pullDirection = pullDirection,
                boxStates = new List<Vector2Int>(boxStates),
                visited = new HashSet<Vector2Int>(visited),
                memory = CloneAutoGenerationMemory(memory)
            };
        }

        private bool TryRollbackAutoGeneration(
            System.Random random,
            List<AutoGenerationSnapshot> snapshots,
            List<Vector2Int> boxStates,
            ref HashSet<Vector2Int> visited,
            ref AutoGenerationMemory memory,
            ref int currentBoxIndex,
            ref Vector2Int playerPosition,
            ref Direction pullDirection)
        {
            if (snapshots.Count == 0)
            {
                return false;
            }

            var rollbackSteps = random.Next(1, Mathf.Min(AutoGenerationRollbackWindow, snapshots.Count) + 1);
            var snapshotIndex = snapshots.Count - rollbackSteps;
            var snapshot = snapshots[snapshotIndex];

            if (generatedReverseActions.Count > snapshot.actionCount)
            {
                generatedReverseActions.RemoveRange(snapshot.actionCount, generatedReverseActions.Count - snapshot.actionCount);
            }

            boxStates.Clear();
            boxStates.AddRange(snapshot.boxStates);
            visited = new HashSet<Vector2Int>(snapshot.visited);
            memory = CloneAutoGenerationMemory(snapshot.memory);
            currentBoxIndex = snapshot.currentBoxIndex;
            playerPosition = snapshot.playerPosition;
            pullDirection = snapshot.pullDirection;
            snapshots.RemoveRange(snapshotIndex, snapshots.Count - snapshotIndex);
            return true;
        }

        private static AutoGenerationMemory CloneAutoGenerationMemory(AutoGenerationMemory source)
        {
            var clone = new AutoGenerationMemory
            {
                lastPulledBoxIndex = source.lastPulledBoxIndex,
                lastBoxFrom = source.lastBoxFrom,
                lastBoxTo = source.lastBoxTo
            };

            foreach (var pair in source.boxHistory)
            {
                clone.boxHistory[pair.Key] = new List<Vector2Int>(pair.Value);
            }

            foreach (var pair in source.cellVisits)
            {
                clone.cellVisits[pair.Key] = pair.Value;
            }

            foreach (var boxIndex in source.returnedToGoalBoxes)
            {
                clone.returnedToGoalBoxes.Add(boxIndex);
            }

            return clone;
        }

        private void AddRandomInitialWalls(System.Random random, HashSet<Vector2Int> reserved)
        {
            for (var y = 0; y < height; y++)
            {
                for (var x = 0; x < width; x++)
                {
                    var position = new Vector2Int(x, y);
                    if (reserved.Contains(position) || random.NextDouble() > autoInitialWallDensity)
                    {
                        continue;
                    }

                    tiles[x, y] = BaseTileType.Wall;
                }
            }
        }

        private void FillUnvisitedWalls(System.Random random, HashSet<Vector2Int> visited, List<Vector2Int> boxStates)
        {
            var protectedCells = new HashSet<Vector2Int>(visited);
            for (var i = 0; i < boxStates.Count; i++)
            {
                protectedCells.Add(boxStates[i]);
            }

            foreach (var goal in goals)
            {
                protectedCells.Add(goal);
            }

            for (var y = 0; y < height; y++)
            {
                for (var x = 0; x < width; x++)
                {
                    var position = new Vector2Int(x, y);
                    if (protectedCells.Contains(position) || tiles[x, y] == BaseTileType.Wall)
                    {
                        continue;
                    }

                    if (random.NextDouble() <= autoUnvisitedWallChance)
                    {
                        tiles[x, y] = BaseTileType.Wall;
                    }
                }
            }
        }

        private int PickAutoAction(System.Random random)
        {
            var total = autoContinueWeight + autoChangeDirectionWeight + autoChangeBoxWeight;
            if (total <= 0f)
            {
                return 0;
            }

            var roll = random.NextDouble() * total;
            if (roll < autoContinueWeight)
            {
                return 0;
            }

            roll -= autoContinueWeight;
            return roll < autoChangeDirectionWeight ? 1 : 2;
        }

        private bool TryAutoPullCurrentBox(
            List<Vector2Int> boxStates,
            List<Vector2Int> targetPositions,
            HashSet<Vector2Int> visited,
            AutoGenerationMemory memory,
            int boxIndex,
            ref Vector2Int playerPosition,
            Direction pullDirection,
            bool avoidDirectReverse)
        {
            var offset = DirectionUtility.ToOffset(pullDirection);
            var boxPosition = boxStates[boxIndex];
            if (playerPosition != boxPosition - offset)
            {
                return false;
            }

            if (avoidDirectReverse && HasReturnedToGoal(memory, boxIndex))
            {
                return false;
            }

            var playerTarget = playerPosition - offset;
            if (!CanAutoOccupy(playerTarget, boxStates))
            {
                return false;
            }

            if (avoidDirectReverse && IsDirectReversePull(memory, boxIndex, playerPosition))
            {
                return false;
            }

            if (avoidDirectReverse && IsRecentBoxOscillation(memory, boxIndex, playerPosition))
            {
                return false;
            }

            var action = new AutoGeneratedAction
            {
                kind = AutoActionKind.Pull,
                boxId = boxIndex,
                direction = pullDirection,
                playerFrom = playerPosition,
                playerTo = playerTarget,
                boxFrom = boxPosition,
                boxTo = playerPosition
            };

            boxStates[boxIndex] = playerPosition;
            playerPosition = playerTarget;
            visited.Add(action.playerFrom);
            visited.Add(action.playerTo);
            visited.Add(action.boxFrom);
            visited.Add(action.boxTo);
            RegisterVisitedCell(memory, action.playerFrom);
            RegisterVisitedCell(memory, action.playerTo);
            RegisterVisitedCell(memory, action.boxFrom);
            RegisterVisitedCell(memory, action.boxTo);
            generatedReverseActions.Add(action);
            memory.lastPulledBoxIndex = boxIndex;
            memory.lastBoxFrom = action.boxFrom;
            memory.lastBoxTo = action.boxTo;
            RecordBoxPosition(memory, boxIndex, action.boxTo);
            UpdateReturnedToGoalState(memory, targetPositions, boxIndex, action.boxTo);
            return true;
        }

        private bool TryAutoChangeDirection(System.Random random, List<Vector2Int> boxStates, List<Vector2Int> targetPositions, HashSet<Vector2Int> visited, AutoGenerationMemory memory, int boxIndex, ref Vector2Int playerPosition, out Direction pullDirection)
        {
            pullDirection = Direction.Up;
            var candidates = GetSortedStandCandidates(random, boxStates, targetPositions, visited, memory, boxIndex, playerPosition, true);
            var currentPlayerPosition = playerPosition;
            candidates.RemoveAll(candidate => candidate.option.position == currentPlayerPosition);
            if (!TryPickAutoStandCandidate(random, candidates, out var candidate))
            {
                return false;
            }

            var option = candidate.option;
            AppendMovePath(candidate.path, visited, memory);
            playerPosition = option.position;
            pullDirection = option.direction;
            return TryAutoPullCurrentBox(boxStates, targetPositions, visited, memory, boxIndex, ref playerPosition, pullDirection, false);
        }

        private bool TryAutoSwitchBox(System.Random random, List<Vector2Int> boxStates, List<Vector2Int> targetPositions, HashSet<Vector2Int> visited, AutoGenerationMemory memory, ref int currentBoxIndex, ref Vector2Int playerPosition, out Direction pullDirection)
        {
            pullDirection = Direction.Up;
            var candidates = new List<AutoStandCandidate>();
            for (var i = 0; i < boxStates.Count; i++)
            {
                if (i != currentBoxIndex)
                {
                    candidates.AddRange(GetSortedStandCandidates(random, boxStates, targetPositions, visited, memory, i, playerPosition, true));
                }
            }

            candidates.Sort((left, right) => right.score.CompareTo(left.score));
            if (!TryPickAutoStandCandidate(random, candidates, out var candidate))
            {
                return false;
            }

            var option = candidate.option;
            AppendMovePath(candidate.path, visited, memory);
            currentBoxIndex = candidate.boxIndex;
            playerPosition = option.position;
            pullDirection = option.direction;
            return TryAutoPullCurrentBox(boxStates, targetPositions, visited, memory, currentBoxIndex, ref playerPosition, pullDirection, false);
        }

        private void AppendMovePath(List<Vector2Int> path, HashSet<Vector2Int> visited, AutoGenerationMemory memory)
        {
            for (var i = 1; i < path.Count; i++)
            {
                var from = path[i - 1];
                var to = path[i];
                generatedReverseActions.Add(new AutoGeneratedAction
                {
                    kind = AutoActionKind.Move,
                    boxId = -1,
                    direction = DirectionFromOffset(to - from),
                    playerFrom = from,
                    playerTo = to,
                    boxFrom = Vector2Int.zero,
                    boxTo = Vector2Int.zero
                });
                visited.Add(from);
                visited.Add(to);
                RegisterVisitedCell(memory, from);
                RegisterVisitedCell(memory, to);
            }
        }

        private List<StandOption> GetStandOptions(List<Vector2Int> boxStates, int boxIndex)
        {
            var options = new List<StandOption>();
            var boxPosition = boxStates[boxIndex];
            AddStandOption(options, boxStates, boxPosition, Direction.Up);
            AddStandOption(options, boxStates, boxPosition, Direction.Down);
            AddStandOption(options, boxStates, boxPosition, Direction.Left);
            AddStandOption(options, boxStates, boxPosition, Direction.Right);
            return options;
        }

        private void AddStandOption(List<StandOption> options, List<Vector2Int> boxStates, Vector2Int boxPosition, Direction direction)
        {
            var offset = DirectionUtility.ToOffset(direction);
            var stand = boxPosition - offset;
            var option = new StandOption { position = stand, direction = direction };
            if (CanAutoOccupy(stand, boxStates) && CanAutoPullFromStand(boxStates, boxPosition, option))
            {
                options.Add(option);
            }
        }

        private List<AutoStandCandidate> GetSortedStandCandidates(
            System.Random random,
            List<Vector2Int> boxStates,
            List<Vector2Int> targetPositions,
            HashSet<Vector2Int> visited,
            AutoGenerationMemory memory,
            int boxIndex,
            Vector2Int playerPosition,
            bool skipDirectReverse)
        {
            var candidates = new List<AutoStandCandidate>();
            var options = GetStandOptions(boxStates, boxIndex);
            Shuffle(random, options);

            for (var i = 0; i < options.Count; i++)
            {
                var option = options[i];
                if (!CanAutoPullFromStand(boxStates, boxIndex, option))
                {
                    continue;
                }

                if (skipDirectReverse && IsDirectReversePull(memory, boxIndex, option.position))
                {
                    continue;
                }

                if (!TryFindPath(playerPosition, option.position, boxStates, out var path))
                {
                    continue;
                }

                candidates.Add(new AutoStandCandidate
                {
                    boxIndex = boxIndex,
                    option = option,
                    path = path,
                    score = ScoreAutoStandCandidate(boxStates, targetPositions, visited, memory, boxIndex, option, path)
                });
            }

            candidates.Sort((left, right) => right.score.CompareTo(left.score));
            return candidates;
        }

        private float ScoreAutoStandCandidate(
            List<Vector2Int> boxStates,
            List<Vector2Int> targetPositions,
            HashSet<Vector2Int> visited,
            AutoGenerationMemory memory,
            int boxIndex,
            StandOption option,
            List<Vector2Int> path)
        {
            var score = GetAutoPullDistanceScore(boxStates, targetPositions, boxIndex, option) * 10f;
            var nextBoxPosition = option.position;
            score += CountFreshPathCells(path, visited) * 3f;
            score -= CountVisitedPathCells(path, visited) * 2f;
            score -= GetPathVisitHeat(path, memory) * 2.5f;
            score += GetBoxSpreadScore(boxStates, boxIndex, nextBoxPosition) * 1.5f;

            if (HasReturnedToGoal(memory, boxIndex))
            {
                score -= 900f;
            }

            if (HasBoxVisitedPosition(memory, boxIndex, nextBoxPosition))
            {
                score -= 120f;
            }

            if (IsDirectReversePull(memory, boxIndex, option.position))
            {
                score -= 1000f;
            }

            if (IsRecentBoxOscillation(memory, boxIndex, nextBoxPosition))
            {
                score -= 650f;
            }

            return score;
        }

        private bool TryPickAutoStandCandidate(System.Random random, List<AutoStandCandidate> candidates, out AutoStandCandidate candidate)
        {
            candidate = default;
            if (candidates.Count == 0)
            {
                return false;
            }

            var poolSize = Mathf.Min(AutoGenerationCandidatePoolSize, candidates.Count);
            var lowestScore = candidates[poolSize - 1].score;
            var totalWeight = 0f;
            for (var i = 0; i < poolSize; i++)
            {
                totalWeight += Mathf.Max(1f, candidates[i].score - lowestScore + 1f);
            }

            var roll = random.NextDouble() * totalWeight;
            for (var i = 0; i < poolSize; i++)
            {
                roll -= Mathf.Max(1f, candidates[i].score - lowestScore + 1f);
                if (roll <= 0)
                {
                    candidate = candidates[i];
                    return true;
                }
            }

            candidate = candidates[poolSize - 1];
            return true;
        }

        private void UpdateReturnedToGoalState(AutoGenerationMemory memory, List<Vector2Int> targetPositions, int boxIndex, Vector2Int boxPosition)
        {
            if (targetPositions == null || boxIndex < 0 || boxIndex >= targetPositions.Count)
            {
                return;
            }

            if (!memory.boxHistory.TryGetValue(boxIndex, out var history) || history.Count <= 1)
            {
                return;
            }

            if (boxPosition == targetPositions[boxIndex])
            {
                memory.returnedToGoalBoxes.Add(boxIndex);
            }
        }

        private bool HasReturnedToGoal(AutoGenerationMemory memory, int boxIndex)
        {
            return memory.returnedToGoalBoxes.Contains(boxIndex);
        }

        private void RecordBoxPosition(AutoGenerationMemory memory, int boxIndex, Vector2Int position)
        {
            if (!memory.boxHistory.TryGetValue(boxIndex, out var history))
            {
                history = new List<Vector2Int>();
                memory.boxHistory[boxIndex] = history;
            }

            history.Add(position);
            if (history.Count > AutoGenerationBoxHistoryLimit)
            {
                history.RemoveAt(0);
            }
        }

